    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String a1 = jTextField1.getText();
        String sql = "select * from ISSUE where MEMBERID = '" + a1 + "'";
        try {
            pst = con.prepareStatement(sql);
            rs = pst.executeQuery();
            if (rs.next()) {

                jTextField2.setText(rs.getString(4));
                jTextField6.setText(rs.getString(1));
                jTextField4.setText(rs.getString(2));
                jTextField3.setText(rs.getString(5));

                rs.close();
                pst.close();

            } else {
                JOptionPane.showMessageDialog(null, "The Given Member Id is Incorrect");
            }

        } catch (Exception sq) {
            sq.printStackTrace();
            JOptionPane.showMessageDialog(null, sq);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

