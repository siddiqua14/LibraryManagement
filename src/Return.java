    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed

        String sql = "INSERT INTO SIGNUP.\"RETURN\" (ID, \"NAME\", BOOKID, BOOKNAME, ISSUEDATE, RETURNDATE) VALUES (?, ?, ?, ?, ?, ?)";

        try {

            pst = con.prepareStatement(sql);
            pst.setString(1, jTextField1.getText());
            pst.setString(2, jTextField2.getText());
            pst.setString(3, jTextField6.getText());
            pst.setString(4, jTextField4.getText());
            pst.setString(5, jTextField3.getText());

            String dob;
            SimpleDateFormat sdf = new SimpleDateFormat("yyyy-mm-dd");
            dob = sdf.format(jDateChooser2.getDate());
            pst.setString(6, dob);
            pst.execute();
            JOptionPane.showMessageDialog(null, "Book Returned!");

            delete();
            clear();

        } catch (SQLException sq) {
            sq.printStackTrace();
            JOptionPane.showMessageDialog(null, sq);
        }
        Return rgf = new Return();
        rgf.setVisible(true);
        rgf.pack();
        rgf.setLocationRelativeTo(null);
        rgf.setDefaultCloseOperation(JFrame.EXIT_ON_CLOSE);
        this.dispose();
    }//GEN-LAST:event_jButton2ActionPerformed

