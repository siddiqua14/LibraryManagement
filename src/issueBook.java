    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        SimpleDateFormat dFormate = new SimpleDateFormat("dd-MM-yyyy");
        String bookID = jTextFieldid.getText();
        String memberID = jTextmid.getText();
        String bookName = jTextBname.getText();
        String memberName = jTextmname.getText();
        String issueDate = dFormate.format(jDateChooser1.getDate());
        String dueDate = dFormate.format(jDateChooser2.getDate());
        //String returnbook = "no";
        try{
            Statement st = con.createStatement();
            ResultSet rs= st.executeQuery("SELECT * FROM NEWBOOK WHERE BOOKID= '"+bookID+"'");
            if(rs.next())
            {
                ResultSet rs1= st.executeQuery("SELECT * FROM MEMBER WHERE ID= '"+memberID+"'");
                if(rs1.next())
                {
                    st.executeUpdate("INSERT INTO ISSUE VALUES('"+bookID+"','"+bookName+"','"+memberID+"','"+memberName+"','"+issueDate+"','"+dueDate+"')");
                    JOptionPane.showMessageDialog(null,"Book successfully issued");
                    //setVisible(false);
                    //new issueBook().setVisible(true);
                }
                else
                JOptionPane.showMessageDialog(null,"Incorrect MemberID");
            }else
            JOptionPane.showMessageDialog(null,"Incorrect BookID");

        }catch (SQLException sq){
            sq.printStackTrace();
            JOptionPane.showMessageDialog(null,"Connection Error");
            setVisible(false);
        }
        issueBook rgf = new issueBook();
        rgf.setVisible(true);
        rgf.pack();
        rgf.setLocationRelativeTo(null);
        rgf.setDefaultCloseOperation(JFrame.EXIT_ON_CLOSE);
        this.dispose();
    }//GEN-LAST:event_jButton1ActionPerformed

