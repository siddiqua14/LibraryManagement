    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        try {

            String sql = "SELECT * FROM SIGNUP.NEWBOOK WHERE BOOKID= ?";
            PreparedStatement pst = con.prepareStatement(sql);
            pst.setString(1, jTextField3.getText());
            ResultSet rs = pst.executeQuery();
            if (rs.next()) {
                //String value=rs.getString("ID");
                jTextID.setText(rs.getString("BOOKID"));
                jTextname.setText(rs.getString("NAME"));
                jTextp.setText(rs.getString("PUBLISHER"));
                jTextpy.setText(rs.getString("PUBLISHERYEAR"));
           

            }
        } catch (Exception ex) {
            ex.printStackTrace();
            JOptionPane.showMessageDialog(null, " Error");
        }
    }//GEN-LAST:event_jButton2ActionPerformed

