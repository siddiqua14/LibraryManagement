    private void jButtonEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonEditActionPerformed

        try {
            Integer id = Integer.parseInt(jTextID.getText());
            String name = jTextname.getText();
            String p = jTextp.getText();
            String py = jTextpy.getText();
           
            String sql = "UPDATE SIGNUP.NEWBOOK SET bookid='" + id + "', NAME='" + name + "', publisher='" + p+ "',publisheryear='" + py + "' WHERE bookid='" + id + "' ";
            PreparedStatement pst = con.prepareStatement(sql);
            pst.executeUpdate();

            JOptionPane.showMessageDialog(null, "Successfully Updated");
        } catch (SQLException ex) {
            ex.printStackTrace();
            JOptionPane.showMessageDialog(null, "Updation Error");
        }
        //showAll();
        EditBook nb = new EditBook();
        nb.setVisible(true);
        nb.pack();
        nb.setLocationRelativeTo(null);
        nb.setDefaultCloseOperation(JFrame.EXIT_ON_CLOSE);
        this.dispose();
    }//GEN-LAST:event_jButtonEditActionPerformed

